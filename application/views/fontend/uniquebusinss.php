                Ｈａｐｐｙは日本ではトミザワグループとして、関東を中心に貸倉庫から始まり、居酒屋やバッテイングセンターまで営む多角企業です。中古オフィス家具も事務所の移転や拡大、縮小にともない発生する家具を預かったりしているうちに始まりました。<br />
                日本では中古品は廃棄処分にされるものも多くあるので、タイでリサイクル販売してみたらどうかと思い始めました。 </p></td>

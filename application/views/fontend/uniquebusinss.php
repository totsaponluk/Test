                日本からはコンテナで輸入します。まず店舗隣接の工場で、きれいに清掃し痛んだところを補修します。<br />
                取扱い商品は、机、椅子、キャビネ、本棚、サイドテーブルなどです。料金は日本の小売価格の１ / ３くらいの値段です。<br />
                チラシ等を見て日本のお客様も多く訪れますが、幹線街道沿いに目立つ店構えなので通りかかったタイ人のお客様が覗いて購入されるケースもあります。今後さらにタイ人の中に広めようと新聞やＨＰで宣伝をしていく予定です。 </p></td>
